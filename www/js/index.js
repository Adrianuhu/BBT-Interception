function mirror() {
    for (var id in interceptionData) {
        var key = id.split("x")[1] + "x" + id.split("x")[0];
        var tableClone = [];

        for (let x = 0; x < 15; x++) {
            var arr = [];
            for (let y = 14; y >= 0; y--) {
                arr.push(interceptionData[id][y][x]);
            }
            tableClone.push(arr);
        }
        interceptionData[key] = tableClone.reverse();
    }
}

