function crearIntercepcion(cont) {
    var rows = 15;
    var columns = 15;

    var mainContainer = document.createElement('div');
    cont.appendChild(mainContainer);
    mainContainer.classList = "crearIntercepcion";

    // Vertical Controlers
    var verticalControlers = document.createElement('div');
    mainContainer.appendChild(verticalControlers);
    verticalControlers.classList = "verticalControlers";

    var plusButton = document.createElement('div');
    verticalControlers.appendChild(plusButton);
    plusButton.classList = "squareYellowButton positionUp";
    plusButton.addEventListener("click", function() {
        var verticalInput = document.getElementsByClassName("verticalInput")[0];
        var horizontalInput = document.getElementsByClassName("horizontalInput")[0];
        if (verticalInput.innerHTML < 13) {
            verticalInput.innerHTML = parseInt(verticalInput.innerHTML) + 1;
            changeInterception(horizontalInput.innerHTML, verticalInput.innerHTML);
        }
    });

    var text = document.createElement('div');
    verticalControlers.appendChild(text);
    text.classList = "inputInterception";
    text.className += " verticalInput";
    text.innerHTML = 1;

    var minusButton = document.createElement('div');
    verticalControlers.appendChild(minusButton);
    minusButton.classList = "squareYellowButton positionDown";
    minusButton.addEventListener("click", function() {
        var verticalInput = document.getElementsByClassName("verticalInput")[0];
        var horizontalInput = document.getElementsByClassName("horizontalInput")[0];
        if (verticalInput.innerHTML >= 1) {
            verticalInput.innerHTML = parseInt(verticalInput.innerHTML) - 1;
            changeInterception(horizontalInput.innerHTML, verticalInput.innerHTML);
        }
    });


    // Second Container
    var secondContainer = document.createElement('div');
    mainContainer.appendChild(secondContainer);
    secondContainer.classList = "secondContainer";

    // Horizontal Controlers
    var horizontalControlers = document.createElement('div');
    secondContainer.appendChild(horizontalControlers);
    horizontalControlers.classList = "horizontalControlers";

    var plusButton = document.createElement('div');
    horizontalControlers.appendChild(plusButton);
    plusButton.classList = "squareYellowButton positionLeft";
    plusButton.addEventListener("click", function() {
        var verticalInput = document.getElementsByClassName("verticalInput")[0];
        var horizontalInput = document.getElementsByClassName("horizontalInput")[0];
        if (horizontalInput.innerHTML > 0) {
            horizontalInput.innerHTML = parseInt(horizontalInput.innerHTML) - 1;
            changeInterception(horizontalInput.innerHTML, verticalInput.innerHTML);
        }
    });

    var text = document.createElement('div');
    horizontalControlers.appendChild(text);
    text.classList = "inputInterception";
    text.className += " horizontalInput";
    text.innerHTML = 0;

    var minusButton = document.createElement('div');
    horizontalControlers.appendChild(minusButton);
    minusButton.classList = "squareYellowButton positionRight";
    minusButton.addEventListener("click", function() {
        var verticalInput = document.getElementsByClassName("verticalInput")[0];
        var horizontalInput = document.getElementsByClassName("horizontalInput")[0];
        if (horizontalInput.innerHTML < 13) {
            horizontalInput.innerHTML = parseInt(horizontalInput.innerHTML) + 1;
            changeInterception(horizontalInput.innerHTML, verticalInput.innerHTML);
        }

    });


    // Create table
    var table = document.createElement('table');
    secondContainer.appendChild(table);
    table.id = "interceptionTable";

    for (let i = 0; i < rows; i++) {
        var row = document.createElement('tr');
        table.appendChild(row);

        for (let j = 0; j < columns; j++) {
            var cell = document.createElement('td');
            row.appendChild(cell);

            if (i == (rows - 1) && j > 0) {
                cell.innerHTML = j - 1;
            } else if (j == 0 && i < (rows - 1)) {
                cell.innerHTML = rows - i - 2;
            } else if (j > 0) {
                // Add interactivity
                cell.addEventListener("click", function() {
                    changeInterception((j - 1), (rows - i - 2));
                    document.getElementsByClassName("horizontalInput")[0].innerHTML = (j - 1);
                    document.getElementsByClassName("verticalInput")[0].innerHTML = (rows - i - 2);
                }, false);
            }
        }
    }

    // Button Change Mode
    var buttonChangeMode = document.createElement('div');
    secondContainer.appendChild(buttonChangeMode);
    buttonChangeMode.classList = "buttonYellow";
    buttonChangeMode.innerHTML = "";
    buttonChangeMode.setAttribute("state", "IT");

    buttonChangeMode.addEventListener("click", function() {
        changePassTable();

        if (this.getAttribute("state") == "IT") {
            buttonChangeMode.setAttribute("state", "PT");
        } else {
            buttonChangeMode.setAttribute("state", "IT");
            changeInterception(0, 1);
        }
    });

    // Set initial state
    changeInterception(0, 1);
}

function changeInterception(horizontalDistance, verticalDistance) {
    // Read data and draw
    var interceptionTable = document.getElementById('interceptionTable');
    var rows = interceptionTable.children;

    for (let i = 0; i < rows.length; i++) {
        var cell = rows[i].children;
        for (let j = 0; j < cell.length; j++) {

            // Reset all cells
            if (cell[j].innerHTML == "X") {
                cell[j].innerHTML = "";
            } else if (cell[j].innerHTML == "O") {
                cell[j].innerHTML = "";
            }
            cell[j].style.backgroundColor = "white";

            var resultData;

            try {
                resultData = interceptionData[horizontalDistance + "x" + verticalDistance][i][j];
                if (resultData == "X") {
                    cell[j].style.backgroundColor = "red";
                    cell[j].innerHTML = "X";
                    cell[j].style.color = "black";
                } else if (resultData == "O") {
                    cell[j].style.backgroundColor = "red";
                    cell[j].innerHTML = "O";
                } else if (resultData == 1) {
                    cell[j].style.backgroundColor = "yellow";
                }
            } catch (error) {
                // Hide if not exist table

                resultData = interceptionData["default"][i][j];

                if (resultData == "X") {
                    cell[j].style.backgroundColor = "#222";
                    cell[j].style.color = "white";
                    cell[j].innerHTML = "X";

                } else if (resultData == 1) {
                    cell[j].style.backgroundColor = "#fdcf41";
                }
            }
        }
    }
}

function changePassTable() {
    var interceptionTable = document.getElementById('interceptionTable');
    var rows = interceptionTable.children;

    for (let i = 0; i < rows.length; i++) {
        var cell = rows[i].children;
        for (let j = 0; j < cell.length; j++) {

            if (cell[j].innerHTML == "X") {
                cell[j].innerHTML = "";
            } else if (cell[j].innerHTML == "O") {
                cell[j].innerHTML = "";
            }
            cell[j].style.backgroundColor = "white";

            const resultData = passRange[i][j];

            if (resultData == "x") {
                cell[j].style.backgroundColor = "black";
                cell[j].style.color = "white";
            } else if (resultData == 1) {
                cell[j].style.backgroundColor = "green";
            } else if (resultData == 2) {
                cell[j].style.backgroundColor = "yellow";
            } else if (resultData == 3) {
                cell[j].style.backgroundColor = "orange";
            } else if (resultData == 4) {
                cell[j].style.backgroundColor = "red";
            }

        }
    }
}


